    ax.set_xlabel('x')
    ax.set_ylabel('y')
    ax.set_zlabel('Basis Function Value')
    ax.set_title(f'2D Lagrange Basis Functions (Order {order}) - Combined View')
    ax.legend()
    ax.view_init(elev=30, azim=-60)
    
    plt.tight_layout()
    
    # Save plot
    output_file = f'2D_lagrange_basis_order_{order}_combined_plot.png'
    plt.savefig(output_file, dpi=300, bbox_inches='tight')
    print(f"Saved: {output_file}")

