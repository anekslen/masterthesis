        # Place DOF at correct position based on 2D numbering
        if i == 0:
            dof_x, dof_y = 0.0, 0.0
        elif i == 1:
            dof_x, dof_y = 1.0, 0.0
        elif i == 2:
            dof_x, dof_y = 1.0, 1.0
        elif i == 3:
            dof_x, dof_y = 0.0, 1.0
        elif i < 4 + (order - 1):  # x edges at y=0
            dof_x = (i - 3) * (1.0/order)
            dof_y = 0.0
        elif i < 4 + 2 * (order - 1):  # x edges at y=1
            dof_x = (i - 3 - (order - 1)) * (1.0/order)
            dof_y = 1.0
        elif i < 4 + 3 * (order - 1):  # y edges at x=0
            dof_x = 0.0
            dof_y = (i - 3 - 2 * (order - 1)) * (1.0/order)
        elif i < 4 + 4 * (order - 1):  # y edges at x=1
            dof_x = 1.0
